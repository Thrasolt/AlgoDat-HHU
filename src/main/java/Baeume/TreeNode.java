public class TreeNode{

  // Instanzvariablen
  private String value;
  private int key;

  private TreeNode left;
  private TreeNode right;

  // Methoden
  public int getKey(){
    return this.key;
  }

  public TreeNode getLeft(){
    return this.left;
  }

  public TreeNode getRight(){
    return this.right;
  }
}
